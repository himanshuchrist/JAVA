    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String from,to,jydate,seat;
        int numberofseats;
        from=origin.getSelectedItem().toString();
        to=destination.getSelectedItem().toString();
        jydate=jdate.getText();
        seat=seattype.getSelectedItem().toString();
        numberofseats=Integer.parseInt(seats.getText());
           try {
            pat=con.prepareStatement("insert into data(fromplace,toplace,departuredate,class,tickets) values(?,?,?,?,?)");
            pat.setString(1,from);
            pat.setString(2,to);
            pat.setString(3,jydate);
            pat.setString(4,seat);
            pat.setInt(5,numberofseats);
            pat.executeUpdate();
            JOptionPane.showMessageDialog(this,"Booking Done");
            Load();
            origin.setSelectedIndex(0);
            destination.setSelectedIndex(0);
            jdate.setText("");
            seattype.setSelectedIndex(0);
            seats.setText("");
            result.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(Lab8.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

