    public void Load(){
        try {
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
            pat=con.prepareStatement("select * from data");
            ResultSet rs=pat.executeQuery();
            ResultSetMetaData rss=rs.getMetaData();
            int c;
            c=rss.getColumnCount();
            def=(DefaultTableModel)jTable1.getModel();
            def.setRowCount(0);
            while(rs.next()){
                String date = rs.getString("departuredate");
                Date date1;
                try {
                    date1 = simpleDateFormat.parse(date);
                } catch (ParseException ex) {
                    Logger.getLogger(Busticket.class.getName()).log(Level.SEVERE, null, ex);
                    return;
                }
                if(new Date().before(date1)){
                        Vector v=new Vector();
                    for(int i=0;i<c;i++){
                        v.add(rs.getString("id"));
                        v.add(rs.getString("departuredate"));
                        v.add(rs.getString("fromplace"));
                        v.add(rs.getString("toplace"));
                        v.add(rs.getString("class"));
                        v.add(rs.getString("tickets"));
                    }
                    def.addRow(v);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Lab8.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
