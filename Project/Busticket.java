    private void getfareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getfareActionPerformed
        // TODO add your handling code here:
        HashMap<String,Integer> fare=new HashMap<String,Integer>();
        fare.put("Bangalore",3);
        fare.put("Kanyakumari",1);
        fare.put("Chennai",2);
        fare.put("Hyderabad",4);
        fare.put("Goa",5);
        fare.put("Mumbai",6);
        fare.put("Ahmedabad",7);
        fare.put("Jaipur",8);
        fare.put("Delhi",10);
        fare.put("Patna", 9);
        fare.put("AC-Sleeper", 3);
        fare.put("AC-Seater", 2);
        fare.put("Non AC-Sleeper", 2);
        fare.put("Non AC-Seater", 1);
        String from,to,jydate,seat;
        int numberofseats;
        from=origin.getSelectedItem().toString();
        to=destination.getSelectedItem().toString();
        jydate=jdate.getText();
        seat=seattype.getSelectedItem().toString();
        numberofseats=Integer.parseInt(seats.getText());
        int flag=0;
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
        try{
            Date date1 = simpleDateFormat.parse(jydate);
            if(new Date().after(date1)){
               flag=1;
            }
        }
        catch(Exception exception){
            flag=1;
        }
        if(from.equals(to)){
            JOptionPane.showMessageDialog(this,"Origin and Destination Same");
        }
        else if(numberofseats<1){
            JOptionPane.showMessageDialog(this,"Invalid number of seats");
        }
        else if(flag!=0){
            JOptionPane.showMessageDialog(this,"Invalid Date"); 
        }
        else{
            result.setText("");
            int seatprice=fare.get(seat);
            int v1=fare.get(from);
            int v2=fare.get(to);
            int total=(Math.abs(v1-v2)*200*seatprice);
            result.append("Fare per Person="+total);
            total*=numberofseats;
            result.append("\nTotal Fare="+total);
            int tax=(int) Math.round(total*0.12);
            result.append("\nGST(12%)="+tax);
            int grand=total+tax;
            result.append("\nGrand Total="+grand);
        }
    }//GEN-LAST:event_getfareActionPerformed

